      <h3>Product Overview</h3>
    <p>The digital signage is one driven by our open architecture software philosophy. Adding a digital signage or video-on-demand application shouldn't require you to throw out your existing hardware and software components. </p>
    <p>Digital signage fits seamlessly into how a network operator already manages the other components in the network. In fact, the right digital signage platform will incorporate the data sources and management tools already present in a customer's network.</p>
    <p>We worked hard to ensure that this platform works well with industry standards.</p>
    <h3>CREATE DIGITAL SIGNAGE, VIDEO-ON-DEMAND AND DIGITAL MENU BOARD SCREENS</h3>

<p>The DMB Screen Builder is a web application which lets a user graphically create and publish sets of digital signage, video-on-demand, and digital menu board screens. Using the DMB Screen Builder a user can build and preview multiple channels of screens. </p>
<p>The DMB Screen Builder offers a publishing component where digital assets and screen control information are automatically delivered to a content distribution server for delivery and automatic playback by one or more digital signage players. In addition to our web-based screen builder application, we also offer the DMB Screen Builder as a standalone Windows application. </p>
<p>The Windows version of the Screen Builder allows a network operator the ability to search the user's network for digital signage players and to deliver digital content to each player as necessary. Cilutions will work with you to determine which version of the Screen Builder is right for you.</p>  
<p>If used together with Cilutions hosted network, the Screen Builder will allow users to upload and store their own digital content to be delivered to their suite of Internet-deployed digital signage players. If the Amino set-top box is used as the digital signage player, then uploaded video files will be automatically converted into the correct format for playback on the Amino.</p>



