<h3>Paid Performance to Reinforce the Brand</h3>
<p>Warby Parker uses PPC and paid advertising campaigns to ensure their brand is always present in the SERP. In the early years of their company, this helped increase awareness for their novel purchase experience. Now, it gives Warby Parker the strength they need to fend off a slew of new competitors.</p>

<p>The core of this strategy is aggressive paid keyword buying—Warby Parker goes after all keywords relevant to both their brand and their products. Doing so increases visibility across a number of relevant search queries and helps Warby Parker connect with customers at any stage of their buyer’s journey.And Warby Parker does this with a relatively modest budget when you consider their size. Their competitors have to do a lot more heavy lifting just to be a part of the conversation.</p>

