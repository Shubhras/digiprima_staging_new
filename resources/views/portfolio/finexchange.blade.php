<h3>How did we approach it?</h3>
<p>This platform was a tool that our client was looking for to bridge the gap between the loan providers, seekers and facilitators a.k.a. vendors. It was not a domain we knew much about but our skills to almost dig out solution from any problem thrown to us came into use. </p>

<p>Our client was really busy in seeking the right market for it and wanted to see us develop a quick prototype that he could use. And that’s what was our first step - loan calculator. </p>


<P>A user could just come into the system, put their amount and tenure in and get the lowest EMI possible for them with all details. That was the right start the client needed and then started building a communications model around it. So now along with the calculation, the users could speak to each other, share documents for verifications. Now was the last piece of the puzzle - can we make them enter into a loan contract? And yes we did it in a way it was seamless!</P>





<br>
<br>



<h3>Project Roadmap</h3>


<ul class="new-sec-list">
  <li>We started off by just supporting client by telling him we didn’t have this domain but had the right intent and knowledge of our industry. All we needed was a little guidance in the beginning. Chad was very supportive and made us learn his industry very nicely! </li>
  <li>Next was a POC (proof of concept), which was the tool of calculating the EMI. </li>
<li>Then we thought along with the client - why not connect the users too - leading to another PoC of communications model, which was iterative. </li>
<li>Then was the POC of managing users. And its then we realized there were too many POCs, which we needed to connect, and something we did in 2014 last 2 months. </li>
<li>2015 was a year we looked right into taking this product to the next level - allowing the vendor type users OR the loan facilitators to seek more loan seekers. And we came up with a proposals sending engine which utilized all contacts of the vendor while tracking each such campaign taken. It was worth our time.</li>
<li> We looked at controlling the white label and allowed the admin of SaaS users to make edits to their “theme” and give it a shape. We improved on our analytics and super admin features to have control of all users and accounts. </li>
<li>At this stage we are refining the product to new business standards by working on things like - auto loan suggestor tool, partner OR bank side of tasks where the banks can create various types of loans and make them available. </li>
<li>Vendors can now choose their loan program etc. too. </li>
<li>Future work is all set as we take the drivers seat now.</li>

</ul>


