<h3>What all we did?</h3>

<p>We can really relate to this project now that we’ve grown this product from where we approached it. So we did all as below –</p>
 
<br>

<ul class="new-sec-list">
  <li>Business analysis.</li>
  <li>Project Management.</li>
<li>Designing and UX planning fully. </li>
<li>Development of multiple POC (proof of concepts).</li>
<li>Analyzing and integrating all the multiple POCs</li>
<li>Quality Analysis done keeping in mind the standards of the finance industry.</li>
<li>Product was made a white label and is SaaS. </li>
<li>Now vision of this project is to make it easy and quick for manufacturers, dealers and small businesses to acquire financing for their customerProblem Statements at low rates, fair terms and be able to manage the process simply.</li>

</ul>


<h3>Problem Statement<h3>

<p>Banks are looking for borrowers to use their money, borrowers looking at banks for loan and some 3rd party vendors looking to close the gap between borrowers and lenders. But all individually which causes the classical Chinese whispers problem. </p>

<p>Our problem statement thus is bridging the gap between the loan providers, seekers and facilitators via communication, calculator and management tools.</p>

<br/>
  



