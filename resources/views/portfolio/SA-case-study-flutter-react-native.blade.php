<p>These approximately 200k PPC clicks combine with their 779k organic clicks to get Warby Parker close to a million overall clicks from SEO each month. That’s an insane amount of potential traffic they have the opportunity to convert, with each click directing a search to a targeted product or landing page.</p>

<p>Warby Parker mirrors their organic keyword strategy by going after product name variations and relevant long-tail key phrases in their ads as well.</p>
